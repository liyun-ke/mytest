package com.doume;

public class User {
	
	private String name;
	
	private String age;
	
	private String sex;
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setAge(String age){
		this.age = age;
	}
	
	public String getAge(){
		return age;
	}
	
	public void setSex(String sex){
		this.sex = sex;
	}
	
	public String getSex(){
		return sex;
	}
	
	
	
}
